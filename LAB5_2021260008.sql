--3 without having
WITH BALANCE_FROM_1001 AS(
    SELECT
        BRANCH_NAME,
        BRANCH_CITY,
        BALANCE
    FROM
        BRANCH
        NATURAL JOIN ACCOUNT
    WHERE
        BALANCE <= 1000
)
SELECT
    BRANCH_CITY,
    AVG(BALANCE)
FROM
    BALANCE_FROM_1001
GROUP BY
    BRANCH_CITY;


--3 with having
SELECT
    BRANCH_CITY,
    AVG(ASSETS)
FROM
    BALANCE_FROM_1001
GROUP BY
    BRANCH_CITY;

--3 with haveing
SELECT
    BRANCH_CITY,
    AVG(ASSETS)
FROM
    BRANCH
GROUP BY
    BRANCH_CITY
HAVING
    SUM(ASSETS) >= 1000;