--3 without having
WITH BALANCE_FROM_1001 AS(
    SELECT
        BRANCH_NAME,
        BRANCH_CITY,
        SUM(BALANCE) AS SUM_BALANCE,
        AVG(BALANCE) AS AVG_BALANCE
    FROM
        BRANCH
        NATURAL JOIN ACCOUNT
    GROUP BY
        BRANCH_NAME,
        BRANCH_CITY
)
SELECT
    BRANCH_CITY,
    AVG(AVG_BALANCE)
FROM
    BALANCE_FROM_1001 BF
WHERE
    BF.SUM_BALANCE >= 1000
GROUP BY BF.BRANCH_CITY;

--3 with having
SELECT
    BRANCH_CITY,
    AVG(TEMP.AVG_B)
FROM
    (
        SELECT
            BRANCH_NAME,
            AVG(BALANCE) AS AVG_B,
            SUM(BALANCE) AS SUM_B
        FROM
            BRANCH
            NATURAL JOIN ACCOUNT
        GROUP BY
            BRANCH_NAME
        HAVING
            SUM(BALANCE) >=1000
    )      TEMP,
    BRANCH B
WHERE
    B.BRANCH_NAME = TEMP.BRANCH_NAME
GROUP BY
    BRANCH_CITY;

--4 without having
WITH TEMP AS(
    SELECT
        BRANCH_CITY,
        BRANCH_NAME,
        AVG(AMOUNT) AS AVG_AM
    FROM
        LOAN
        NATURAL JOIN BRANCH
    GROUP BY
        BRANCH_CITY,
        BRANCH_NAME
)
SELECT
    TEMP2.BRANCH_CITY,
    AVG(AVG_AM)
FROM
    (
        SELECT
            BRANCH_CITY,
            BRANCH_NAME,
            AVG_AM
        FROM
            TEMP
        WHERE
            1500 < AVG_AM
    )TEMP2,      BRANCH B
WHERE
    B.BRANCH_NAME = TEMP2.BRANCH_NAME
GROUP BY
    TEMP2.BRANCH_CITY;

--4 WITH HAVING
SELECT
    BRANCH_CITY,
    AVG(AVG_L)
FROM
    (
        SELECT
            BRANCH_NAME,
            AVG(AMOUNT) AS AVG_L
        FROM
            LOAN
        WHERE
            BRANCH_NAME = LOAN.BRANCH_NAME
        GROUP BY
            BRANCH_NAME
        HAVING
            AVG(AMOUNT) >= 1500
    )      
    NATURAL JOIN BRANCH
GROUP BY
    BRANCH.BRANCH_CITY;

